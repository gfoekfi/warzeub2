#include "font.h"
#include "SDL_ttf.h"
#include <assert.h>


// ============================================================================
// ----------------------------------------------------------------------------
// ============================================================================

bool FontInit()
{
	assert(!TTF_WasInit());

	if (TTF_Init() == -1)
	{
		fprintf(stderr, "TTF_Init: %s\n", TTF_GetError());
		return false;
	}

	return true;
}

// ============================================================================

void FontQuit()
{
	if (TTF_WasInit())
		TTF_Quit();
}

