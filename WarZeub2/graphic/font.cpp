#include "font.h"
#include <assert.h>


// ============================================================================
// ----------------------------------------------------------------------------
// ============================================================================

const size_t FONT_SIZE = 15;
TTF_Font* gArialFont = 0;

// ============================================================================
// ----------------------------------------------------------------------------
// ============================================================================

bool FontInit()
{
	assert(!TTF_WasInit());

	if (TTF_Init() == -1)
	{
		fprintf(stderr, "TTF_Init: %s\n", TTF_GetError());
		return false;
	}

	gArialFont = TTF_OpenFont("../Data/arial.ttf", FONT_SIZE);
	if (!gArialFont)
	{
		fprintf(stderr, "TTF_OpenFont: %s\n", TTF_GetError());
		return false;
	}

	return true;
}

// ============================================================================

void FontQuit()
{
	if (!TTF_WasInit())
	{
		assert(gArialFont == 0);
		return;
	}

	if (gArialFont)
		TTF_CloseFont(gArialFont);

	TTF_Quit();
}

