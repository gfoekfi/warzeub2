#include "map.h"
#include <assert.h>


// ============================================================================
// ----------------------------------------------------------------------------
// ============================================================================

Map::Map(size_t parWidth, size_t parHeight)
	: width_(parWidth),
	height_(parHeight)
{
	assert(width_ > 0);
	assert(height_ > 0);
}

// ============================================================================

Map::~Map()
{
}

// ============================================================================

void Map::TileAlign(float2& parPos)
{
	parPos.x = float(size_t(parPos.x) / MAP_TILE_SIZE) * MAP_TILE_SIZE + 0.5f * MAP_TILE_SIZE;
	parPos.y = float(size_t(parPos.y) / MAP_TILE_SIZE) * MAP_TILE_SIZE + 0.5f * MAP_TILE_SIZE;
}

