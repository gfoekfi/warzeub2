struct Vec2
{
	Vec2(int parX, int parY) :
		x(parX),
		y(parY)
	{
	}

	Vec2(const Vec2& parRhs) :
		x(parRhs.x),
		y(parRhs.y)
	{
	}

	Vec2() :
		x(0),
		y(0)
	{
	}

	Vec2& operator=(Vec2& parRhs)
	{
		x = parRhs.x;
		y = parRhs.y;

		return *this;
	}

	int x;
	int y;
};

