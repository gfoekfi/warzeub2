enum EOrder
{
	EO_NONE			= 0,
	EO_STOP			= 1 << 0,
	EO_CANCEL		= 1 << 1,
	EO_TRAIN_PEON	= 1 << 2,
	EO_MOVE			= 1 << 3,
};

// ============================================================================

struct UnitDesc
{
	UnitDesc(int parWidth, int parHeight, int parOrderMask) :
		width(parWidth),
		height(parHeight),
		orderMask(parOrderMask)
	{
	}

	UnitDesc() :
		width(0),
		height(0),
		orderMask(0)
	{
	}

	int width;
	int height;
	int orderMask;
};

