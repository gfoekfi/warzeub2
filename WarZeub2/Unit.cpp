void Update(Unit& parUnit, Uint32 parCurTime)
{
	Uint32 deltaTime = parCurTime - parUnit.lastTime;
	if (deltaTime > 170)
	{
		parUnit.spriteStep++;
		parUnit.lastTime = parCurTime;
	}
}

